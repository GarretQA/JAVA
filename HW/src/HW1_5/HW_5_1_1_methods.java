package HW1_5;

public class HW_5_1_1_methods {

    public int getSum(int a, int b) {
        return a + b;
    }

    public int getDiff(int a, int b) {
        return a - b;
    }

    public int getMulti(int a, int b) {
        return a * b;
    }

    public double getDiv(double a, double b) {
        return a / b;
    }
}
